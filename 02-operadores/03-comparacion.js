// ESTOS SON VALORES RELACIONALES

let a = 10;          
console.log(a > 5); //Aca pregunto si a es mayor a 5 y me va a devolver que es verdadero(true)

console.log(a >= 5); //true

let b = 5;            

console.log(b > 5);  //false

console.log(b >= 5);  //true

console.log(b < 5);   //false

console.log(b <= 5);  //true


// ESTOS SON VALORES DE IGUALDAD

let c = 20;

console.log(c == 20);  //true.. con == preguntamos si es igual
console.log(c != 20);  //true.. con el signo ! preguntamos si el valor es distinto 
console.log(c == '20'); // true.. pregunto si C es = al valor de string 10

console.log(c === '20'); //false.. aca pregunto si tiene el mismo valor y si tinen el mismo tipo de dato 